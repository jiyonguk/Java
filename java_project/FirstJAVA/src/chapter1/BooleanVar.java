package chapter1;

public class BooleanVar {

	public static void main(String[] args) {
		//자료형 변수를 선언하여 참과 거짓을 저장한다
		boolean b1 = true;
		boolean b2 = false;
		//정수형 변수를 선언하여 수를 저장
		int num = 100;
		//값을 출력한다
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("--------------------------");
		System.out.println(4 > 3);
		System.out.println(4 < 3);
		System.out.println("--------------------------");
		System.out.println(num > 20);


	}
}
